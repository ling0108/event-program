            var dateColumn = document.getElementById("date-column");
var eventColumn = document.getElementById("event-column");
 var dateDetailsColumn = document.getElementById("date-details-column");

            function displayEvents(dateId) {
                var allDates = document.querySelectorAll(".date-item");
                allDates.forEach(function (date) {
                    date.classList.remove("active");
                });

                var activeDate = document.getElementById(dateId);
                activeDate.classList.add("active");

                var allEvents = document.querySelectorAll(".event");
                allEvents.forEach(function (event) {
                    event.classList.remove("active");
                });

                var activeEvents = document.getElementById(dateId + "-events");
                activeEvents.classList.add("active");
                    var allDateDetails = document.querySelectorAll(".date-details");
                allDateDetails.forEach(function (dateDetail) {
                    dateDetail.classList.remove("active");
                });

                var activeDateDetail = document.getElementById(dateId + "-details");
                activeDateDetail.classList.add("active");
            }

  
Object.keys(calendarData.calendar).forEach(function (month) {
                var monthDiv = document.createElement("div");
                monthDiv.classList.add("month");
                monthDiv.innerHTML = "<h2>" + month + "</h2>";
                dateColumn.appendChild(monthDiv);

                var days = calendarData.calendar[month];

                days.forEach(function (day) {
                    var dateDiv = document.createElement("div");
                    dateDiv.classList.add("date-item");
                    dateDiv.setAttribute("id", day.id); 
                    dateDiv.textContent = day.date;
                    dateDiv.addEventListener("click", function () {
                        displayEvents(day.id);
                    });
                    monthDiv.appendChild(dateDiv);

                    var eventsDiv = document.createElement("div");
                    eventsDiv.classList.add("event");
                    eventsDiv.setAttribute("id", day.id + "-events");

                    day.events.forEach(function (event) {
                        var eventDiv = document.createElement("div");
                        eventDiv.classList.add("program");
                        eventDiv.innerHTML = 
                            "<div class='tab'><a href='" + event.taburl + "'>" + event.tab + "</a></div>" +
                                "<div class='title'>" + event.title + "</div>" +
                            "<div class='time'>" + event.time + "</div>" +
                            "<div class='venue'>" + event.venue + "</div>" +
                            "<div class='link'><img class='img-responsive' src='img/linkout.svg'><a href='" + event.link + "'>Link</a></div>";
                        eventsDiv.appendChild(eventDiv);
                    });

                    eventColumn.appendChild(eventsDiv);
                    var dateDetailsDiv = document.createElement("div");
                    dateDetailsDiv.classList.add("date-details");
                    dateDetailsDiv.setAttribute("id", day.id + "-details");
                    dateDetailsDiv.innerHTML = "<p><strong>" + month +" </strong> " + day.date + "</p>";
                    dateDetailsColumn.appendChild(dateDetailsDiv);
                });
            });

